tap_dance_action_t tap_dance_actions[] = {
    [TD_SHIFT_CAPS] = ACTION_TAP_DANCE_FN_ADVANCED_WITH_RELEASE(NULL, NULL, shift_dance_finished_fn, shift_dance_reset_fn),
    [TD_CTRL_ESC] = ACTION_TAP_DANCE_FN_ADVANCED_WITH_RELEASE(NULL, NULL, ctrl_dance_finished_fn, ctrl_dance_reset_fn),
    [TD_ENTER_TAB] = ACTION_TAP_DANCE_DOUBLE(KC_ENTER, KC_TAB),
};

void altgr(uint16_t keycode) {
    register_code(KC_RALT);
    tap_code16(keycode);
    unregister_code(KC_RALT);
}

void shift(uint16_t keycode) {
    register_code(KC_RSFT);
    tap_code16(keycode);
    unregister_code(KC_RSFT);
}

void shift_altgr(uint16_t keycode) {
    register_code(KC_RSFT);
    register_code(KC_RALT);
    tap_code16(keycode);
    unregister_code(KC_RALT);
    unregister_code(KC_RSFT);
}

// Custom keycode handler
bool process_record_user(uint16_t keycode, keyrecord_t* record) {
    /*
     * Current keyboard layout set in the system
     * 0 -> German (Macintosh)
     * 1 -> English (US, international with dead keys)
     */
    static uint16_t system_keyboard_layout = 0;

    // Switch to layer 1 when tapped, momentarily switch to layer 1 when held
    static uint16_t k_layer1_timer;
    if (keycode == K_LAYER1) {
        if (record->event.pressed) {
            k_layer1_timer = timer_read(); // Start the timer when key is pressed
            layer_on(1);
        } else {
            layer_clear();

            if (timer_elapsed(k_layer1_timer) < LAYER_SWITCH_TAPPING_TERM) {
                layer_move(1);
            }
        }
        return false;
    } else {
        k_layer1_timer -= LAYER_SWITCH_TAPPING_TERM;
    }

    if (!record->event.pressed)
        return true;

    switch (keycode) {
        case K_CYCLE_LANGUAGE:
            if (system_keyboard_layout == 0) {
                system_keyboard_layout = 1;
            } else {
                system_keyboard_layout = 0;
            }
            return false;
        case K_TILDE: // "~" symbol
            if (system_keyboard_layout == 0) {
                tap_code16(DE_TILD);
                tap_code16(KC_SPC);
            } else {
                tap_code16(DE_RABK);
            }
            return false;
        case K_GRAVE: // "`" symbol
            if (system_keyboard_layout == 0) {
                shift(DE_ACUT);
                tap_code16(KC_SPC);
            } else {
                tap_code16(KC_LABK);
                tap_code16(KC_SPC);
            }
            return false;
        case K_CIRCUMFLEX: // "^" symbol
            if (system_keyboard_layout == 0) {
                tap_code16(DE_CIRC);
                tap_code16(KC_SPC);
            } else {
                tap_code16(KC_CIRCUMFLEX);
            }
            return false;
        case K_COLON:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_COLN);
            } else {
                tap_code16(KC_COLON);
            }
            return false;
        case K_SEMICOLON:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_SCLN);
            } else {
                tap_code16(KC_SEMICOLON);
            }
            return false;
        case K_AT:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_AT);
            } else {
                tap_code16(KC_AT);
            }
            return false;
        case K_HASH:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_HASH);
            } else {
                tap_code16(KC_HASH);
            }
            return false;
        case K_DOLLAR:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_DLR);
            } else {
                tap_code16(KC_DOLLAR);
            }
            return false;
        case K_PERCENT:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_PERC);
            } else {
                tap_code16(KC_PERCENT);
            }
            return false;
        case K_AND:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_AMPR);
            } else {
                tap_code16(KC_AMPERSAND);
            }
            return false;
        case K_EQUAL:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_EQL);
            } else {
                tap_code16(KC_EQUAL);
            }
            return false;
        case K_UNDERSCORE:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_UNDS);
            } else {
                tap_code16(KC_UNDERSCORE);
            }
            return false;
        case K_PIPE:
            if (system_keyboard_layout == 0) {
                altgr(KC_7);
            } else {
                tap_code16(KC_PIPE);
            }
            return false;
        case K_SLASH:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_SLSH);
            } else {
                tap_code16(KC_SLASH);
            }
            return false;
        case K_BACKSLASH:
            if (system_keyboard_layout == 0) {
                shift_altgr(KC_7);
            } else {
                tap_code16(KC_BACKSLASH);
            }
            return false;
        case K_DOUBLE_QUOTE:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_DQUO);
            } else {
                tap_code16(KC_DOUBLE_QUOTE);
            }
            return false;
        case K_QUOTE:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_QUOT);
            } else {
                tap_code16(KC_QUOTE);
            }
            return false;
        case K_QUESTION:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_QUES);
            } else {
                tap_code16(KC_QUESTION);
            }
            return false;
        case K_EXCLAIM:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_EXLM);
            } else {
                tap_code16(KC_EXCLAIM);
            }
            return false;
        case K_DIACRITIC:
            if (system_keyboard_layout == 0) {
                altgr(KC_U);
            } else {
            }
            return false;
        case K_ESZETT:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_SS);
            } else {
            }
            return false;
        case K_LEFT_BRACKET:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_LPRN);
            } else {
                tap_code16(KC_LEFT_PAREN);
            }
            return false;
        case K_RIGHT_BRACKET:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_RPRN);
            } else {
                tap_code16(KC_RIGHT_PAREN);
            }
            return false;
        case K_LEFT_ANGLE_BRACKET:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_LABK);
            } else {
                tap_code16(KC_LEFT_ANGLE_BRACKET);
            }
            return false;
        case K_RIGHT_ANGLE_BRACKET:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_RABK);
            } else {
                tap_code16(KC_RIGHT_ANGLE_BRACKET);
            }
            return false;
        case K_LEFT_CURLY_BRACKET:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_LCBR);
            } else {
                tap_code16(KC_LEFT_CURLY_BRACE);
            }
            return false;
        case K_RIGHT_CURLY_BRACKET:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_RCBR);
            } else {
                tap_code16(KC_RIGHT_CURLY_BRACE);
            }
            return false;
        case K_LEFT_SQUARE_BRACKET:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_LBRC);
            } else {
                tap_code16(KC_LEFT_BRACKET);
            }
            return false;
        case K_RIGHT_SQUARE_BRACKET:
            if (system_keyboard_layout == 0) {
                tap_code16(DE_RBRC);
            } else {
                tap_code16(KC_RIGHT_BRACKET);
            }
            return false;
        case KC_Y:
            if (system_keyboard_layout == 0) {
                tap_code16(KC_Z);
                return false;
            }
        case KC_Z:
            if (system_keyboard_layout == 0) {
                tap_code16(KC_Y);
                return false;
            }
    }
    return true;
};

